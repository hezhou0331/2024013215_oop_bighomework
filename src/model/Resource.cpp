//Resource 类所属头文件
#include "model/Resource.hpp"

//std::invalid_argument 所属头文件
#include <stdexcept>

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::Resource
//【函数功能】       默认构造资源对象：名称取占位值 "UnnamedResource"，单位成本为 0。
//【参数】           无
//【返回值】         构造函数无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
Resource::Resource()
    : m_Name("UnnamedResource"),
      m_rUnitCost(0.0)
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::Resource
//【函数功能】       以名称与单位成本构造资源对象；名称为空或成本为负时抛出异常。
//【参数】           Name（输入参数）：资源名称，不允许为空字符串；
//                   UnitCost（输入参数）：资源单位时间成本，不允许为负。
//【返回值】         构造函数无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
Resource::Resource(const std::string& Name, double UnitCost)
    : m_Name(Name),
      m_rUnitCost(UnitCost)
{
    if (m_Name.empty()) {                             //资源名称不允许为空
        throw std::invalid_argument("Resource name cannot be empty.");
    }
    if (m_rUnitCost < 0.0) {                          //单位成本不允许为负
        throw std::invalid_argument("Resource unit cost cannot be negative.");
    }
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::Resource
//【函数功能】       拷贝构造资源对象：复制名称与单位成本。
//【参数】           Source（输入参数）：被拷贝的资源对象。
//【返回值】         构造函数无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
Resource::Resource(const Resource& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::operator=
//【函数功能】       拷贝赋值资源对象：复制名称与单位成本。
//【参数】           Source（输入参数）：赋值来源的资源对象。
//【返回值】         返回自身引用，支持连续赋值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
Resource& Resource::operator=(const Resource& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::~Resource
//【函数功能】       析构资源对象；无额外资源需要释放。
//【参数】           无
//【返回值】         析构函数无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
Resource::~Resource() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::GetName
//【函数功能】       返回资源名称。
//【参数】           无
//【返回值】         const std::string&，资源名称的常量引用。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
const std::string& Resource::GetName() const
{
    return m_Name;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::GetUnitCost
//【函数功能】       返回资源单位时间成本。
//【参数】           无
//【返回值】         double，资源单位时间成本，非负实数。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
double Resource::GetUnitCost() const
{
    return m_rUnitCost;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::SetName
//【函数功能】       设置资源名称；名称为空时抛出 std::invalid_argument 且不修改原名称。
//【参数】           Name（输入参数）：新的资源名称，不允许为空字符串。
//【返回值】         void，无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
void Resource::SetName(const std::string& Name)
{
    if (Name.empty()) {                               //资源名称不允许为空
        throw std::invalid_argument("Resource name cannot be empty.");
    }
    m_Name = Name;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::SetUnitCost
//【函数功能】       设置资源单位时间成本；成本为负时抛出 std::invalid_argument 且不修改原值。
//【参数】           UnitCost（输入参数）：新的单位时间成本，不允许为负。
//【返回值】         void，无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
void Resource::SetUnitCost(double UnitCost)
{
    if (UnitCost < 0.0) {                             //单位成本不允许为负
        throw std::invalid_argument("Resource unit cost cannot be negative.");
    }
    m_rUnitCost = UnitCost;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::CalculateCost
//【函数功能】       计算按给定数量占用本资源、持续给定工期的成本：
//                   成本 = 单位成本 * 数量 * 工期；数量非正或工期为负时抛出异常。
//【参数】           Quantity（输入参数）：占用资源的数量，必须为正整数；
//                   Duration（输入参数）：占用持续的工期，非负整数，单位：天。
//【返回值】         double，本次占用产生的成本。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
double Resource::CalculateCost(int Quantity, int Duration) const
{
    if (Quantity <= 0) {                              //占用数量必须为正
        throw std::invalid_argument("Resource quantity must be positive.");
    }
    if (Duration < 0) {                               //工期不允许为负
        throw std::invalid_argument("Duration cannot be negative.");
    }
    return m_rUnitCost * Quantity * Duration;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Resource::HasSameName
//【函数功能】       判断资源名称是否与给定名称完全相同，用于新增资源时查重。
//【参数】           Name（输入参数）：待比较的名称。
//【返回值】         bool，true 表示名称相同，false 表示不同。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
bool Resource::HasSameName(const std::string& Name) const
{
    return m_Name == Name;
}
