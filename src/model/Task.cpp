//【函数名称】       Task::SetSchedule
//【函数功能】       写入 CPM 调度计算得到的四个时间，并按 LS - ES 同步计算时差。
//【参数】           EarlyStart（输入参数）：最早开始时间 ES（天）；
//                   EarlyFinish（输入参数）：最早结束时间 EF（天）；
//                   LateStart（输入参数）：最晚开始时间 LS（天）；
//                   LateFinish（输入参数）：最晚结束时间 LF（天）。
//【返回值】         void，无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】       2026-07-05 形参由缩写 ES/EF/LS/LF 改为全称，符合标识符长度规范。
//-------------------------------------------------------------------------------------------------------------------
void Task::SetSchedule(int EarlyStart, int EarlyFinish,
                       int LateStart, int LateFinish)
{
    m_iEarlyStart = EarlyStart;
    m_iEarlyFinish = EarlyFinish;
    m_iLateStart = LateStart;
    m_iLateFinish = LateFinish;
    m_iSlack = m_iLateStart - m_iEarlyStart;          //时差 = 最晚开始 - 最早开始
}

