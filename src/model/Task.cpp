//【函数名称】       Task::SetName
//【函数功能】       设置任务名称；名称为空时抛出 std::invalid_argument 且不修改原名称。
//【参数】           Name（输入参数）：新的任务名称，不允许为空字符串。
//【返回值】         void，无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
void Task::SetName(const std::string& Name)
{
    if (Name.empty()) {
        throw std::invalid_argument("Task name cannot be empty.");
    }
    //检查名称中是否含有空白字符
    for (char Char : Name) {
        if (std::isspace(static_cast<unsigned char>(Char)) != 0) {
            throw std::invalid_argument(
                "Name must not contain whitespace characters.");
        }
    }
    m_Name = Name;
}

