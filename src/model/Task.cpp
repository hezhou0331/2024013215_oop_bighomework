//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Task::IsCritical
//【函数功能】       判断任务是否位于关键路径上：最早时间与最晚时间重合即为关键任务。
//【参数】           无
//【返回值】         bool，true 表示任务在关键路径上，false 表示不在。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
bool Task::IsCritical() const
{
    return (m_iEarlyFinish == m_iLateFinish) || (m_iEarlyStart == m_iLateStart);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Task::CalculateTotalCost
//【函数功能】       汇总任务总成本：对每条资源分配，按资源单价、占用数量与任务工期
//                   计算成本并累加；分配记录中的资源索引越界时抛出异常。
//【参数】           Resources（输入参数）：项目全部资源的列表，用于按索引查询单价。
//【返回值】         double，任务的资源总成本。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
double Task::CalculateTotalCost(const std::vector<Resource>& Resources) const
{
    double TotalCost = 0.0;                           //累加得到的总成本
    for (const ResourceAllocation& Allocation : m_Resources) {
        if (Allocation.GetResourceIndex() >= Resources.size()) {
            throw std::out_of_range("Resource allocation index is invalid.");
        }
        TotalCost += Resources[Allocation.GetResourceIndex()].CalculateCost(
            Allocation.GetQuantity(),
            GetDuration());
    }
    return TotalCost;
}

