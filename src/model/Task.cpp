//【函数名称】       Task::AddResource
//【函数功能】       为任务分配指定索引的资源：若该资源尚未分配则新增一条分配记录，
//                   已分配则把数量更新为新值；任务不可占资源或数量非正时抛出异常。
//【参数】           ResourceIndex（输入参数）：被分配资源在项目资源容器中的索引；
//                   Quantity（输入参数）：占用该资源的数量，必须为正整数。
//【返回值】         void，无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
void Task::AddResource(std::size_t ResourceIndex, int Quantity)
{
    if (!CanAllocateResource()) {                     //里程碑等任务类型不可占用资源
        throw std::logic_error("This task cannot allocate resources.");
    }
    if (Quantity <= 0) {
        throw std::invalid_argument("Resource quantity must be positive.");
    }

    //查找该资源是否已有分配记录
    auto Iter = std::find_if(
        m_Resources.begin(),
        m_Resources.end(),
        [ResourceIndex](const ResourceAllocation& Allocation) {
            return Allocation.GetResourceIndex() == ResourceIndex;
        });
    if (Iter == m_Resources.end()) {                  //未分配过，新增记录
        m_Resources.push_back(ResourceAllocation(ResourceIndex, Quantity));
    }
    else {                                            //已分配过，仅更新数量
        Iter->SetQuantity(Quantity);
    }
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Task::RemoveResource
//【函数功能】       移除任务对指定索引资源的分配记录；不存在该记录时不做任何操作。
//【参数】           ResourceIndex（输入参数）：待移除资源在项目资源容器中的索引。
//【返回值】         void，无返回值。
//【开发者及日期】   2024013215, 2026-07-05
//【更改记录】
//-------------------------------------------------------------------------------------------------------------------
void Task::RemoveResource(std::size_t ResourceIndex)
{
    m_Resources.erase(
        std::remove_if(
            m_Resources.begin(),
            m_Resources.end(),
            [ResourceIndex](const ResourceAllocation& Allocation) {
                return Allocation.GetResourceIndex() == ResourceIndex;
            }),
        m_Resources.end());
}

