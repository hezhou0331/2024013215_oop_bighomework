#include "model/ProjectRepository.hpp"

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::ProjectRepository
//【函数功能】       默认构造项目仓库：以默认名称 "UntitledProject" 创建空的当前项目。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ProjectRepository::ProjectRepository()
    : m_CurrentProject("UntitledProject")
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::ProjectRepository
//【函数功能】       拷贝构造项目仓库：深拷贝源仓库持有的当前项目。
//【参数】           Source（输入参数）：作为拷贝来源的项目仓库对象。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ProjectRepository::ProjectRepository(const ProjectRepository& Source)
    = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::operator=
//【函数功能】       拷贝赋值：深拷贝源仓库持有的当前项目。
//【参数】           Source（输入参数）：作为赋值来源的项目仓库对象。
//【返回值】         ProjectRepository&，返回自身引用以支持连续赋值。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ProjectRepository& ProjectRepository::operator=(
    const ProjectRepository& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::~ProjectRepository
//【函数功能】       析构项目仓库；当前项目为值成员，无需额外清理。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ProjectRepository::~ProjectRepository() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::GetCurrentProject
//【函数功能】       读取当前项目（可修改版本），供上层控制器编辑项目内容。
//【参数】           无
//【返回值】         Project&，当前项目的可修改引用。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
Project& ProjectRepository::GetCurrentProject()
{
    return m_CurrentProject;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::GetCurrentProject
//【函数功能】       读取当前项目（只读版本），供查询与导出等场景使用。
//【参数】           无
//【返回值】         const Project&，当前项目的只读引用。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
const Project& ProjectRepository::GetCurrentProject() const
{
    return m_CurrentProject;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::SetCurrentProject
//【函数功能】       以拷贝方式整体替换当前项目。
//【参数】           SourceProject（输入参数）：用于替换当前项目的项目对象。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
void ProjectRepository::SetCurrentProject(const Project& SourceProject)
{
    m_CurrentProject = SourceProject;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::SetCurrentProject
//【函数功能】       以移动方式整体替换当前项目，避免深拷贝开销（如导入文件后接管结果）。
//【参数】           SourceProject（输入参数）：用于替换当前项目的项目对象，替换后
//                   处于有效但未指定状态。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
void ProjectRepository::SetCurrentProject(Project&& SourceProject)
{
    m_CurrentProject = std::move(SourceProject);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ProjectRepository::Reset
//【函数功能】       把当前项目重置为默认名称 "UntitledProject" 的空项目（新建项目场景）。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
void ProjectRepository::Reset()
{
    m_CurrentProject = Project("UntitledProject");
}
