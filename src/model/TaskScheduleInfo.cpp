//-------------------------------------------------------------------------------------------------------------------
//【文件名】                 TaskScheduleInfo.cpp
//【功能模块和目的】         实现任务时间参数类：构造并保存单个任务的最早开始/完成、最晚开始/
//                           完成时间与松弛时间，并提供各参数的读取接口。
//【开发者及日期】           刘江宇, 2026-07-05
//【修改记录】               2026-07-05 按课程 C++ 编码规范 V1.3 修订注释与成员命名。
//-------------------------------------------------------------------------------------------------------------------
#include "model/TaskScheduleInfo.hpp"

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::TaskScheduleInfo
//【函数功能】       默认构造任务时间参数，五个时间参数全部初始化为零。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
TaskScheduleInfo::TaskScheduleInfo()
    : m_iEarlyStart(0),
      m_iEarlyFinish(0),
      m_iLateStart(0),
      m_iLateFinish(0),
      m_iSlack(0)
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::TaskScheduleInfo
//【函数功能】       带参构造任务时间参数，记录最早开始/完成、最晚开始/完成与松弛时间。
//【参数】           EarlyStart（输入参数）：最早开始时间（天）；
//                   EarlyFinish（输入参数）：最早完成时间（天）；
//                   LateStart（输入参数）：最晚开始时间（天）；
//                   LateFinish（输入参数）：最晚完成时间（天）；
//                   Slack（输入参数）：松弛时间（天）。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
TaskScheduleInfo::TaskScheduleInfo(int EarlyStart, int EarlyFinish,
                                   int LateStart, int LateFinish, int Slack)
    : m_iEarlyStart(EarlyStart),
      m_iEarlyFinish(EarlyFinish),
      m_iLateStart(LateStart),
      m_iLateFinish(LateFinish),
      m_iSlack(Slack)
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::TaskScheduleInfo
//【函数功能】       拷贝构造任务时间参数，复制全部五个时间参数。
//【参数】           Source（输入参数）：被拷贝的时间参数对象。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
TaskScheduleInfo::TaskScheduleInfo(const TaskScheduleInfo& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::operator=
//【函数功能】       拷贝赋值任务时间参数，复制全部五个时间参数。
//【参数】           Source（输入参数）：赋值来源的时间参数对象。
//【返回值】         返回自身引用，支持连续赋值。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
TaskScheduleInfo& TaskScheduleInfo::operator=(const TaskScheduleInfo& Source)
    = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::~TaskScheduleInfo
//【函数功能】       析构任务时间参数对象；不持有资源，无需额外清理。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
TaskScheduleInfo::~TaskScheduleInfo() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::GetES
//【函数功能】       读取最早开始时间。
//【参数】           无
//【返回值】         返回最早开始时间，单位：天。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
int TaskScheduleInfo::GetES() const
{
    return m_iEarlyStart;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::GetEF
//【函数功能】       读取最早完成时间。
//【参数】           无
//【返回值】         返回最早完成时间，单位：天。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
int TaskScheduleInfo::GetEF() const
{
    return m_iEarlyFinish;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::GetLS
//【函数功能】       读取最晚开始时间。
//【参数】           无
//【返回值】         返回最晚开始时间，单位：天。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
int TaskScheduleInfo::GetLS() const
{
    return m_iLateStart;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::GetLF
//【函数功能】       读取最晚完成时间。
//【参数】           无
//【返回值】         返回最晚完成时间，单位：天。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
int TaskScheduleInfo::GetLF() const
{
    return m_iLateFinish;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       TaskScheduleInfo::GetSlack
//【函数功能】       读取松弛时间。
//【参数】           无
//【返回值】         返回松弛时间，单位：天，为零表示关键任务。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
int TaskScheduleInfo::GetSlack() const
{
    return m_iSlack;
}
