//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::Dependency
//【函数功能】       默认构造依赖关系，初始化为 0 号任务到 0 号任务的 FS、零延迟占位依赖。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
Dependency::Dependency()
    : m_uPredecessor(0),
      m_uSuccessor(0),
      m_Type(Dependency::Type::FINISH_TO_START),
      m_iLag(0)
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::Dependency
//【函数功能】       带参构造依赖关系，记录前置、后置任务下标、依赖类型与延迟天数，
//                   并拒绝前置与后置为同一任务的自依赖。
//【参数】           Predecessor（输入参数）：前置任务下标；
//                   Successor（输入参数）：后置任务下标；
//                   DependencyType（输入参数）：依赖类型（FS/SS/FF/SF）；
//                   LagDays（输入参数）：延迟天数，可为负表示提前。
//【返回值】         无。；自依赖时抛出 std::invalid_argument。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
Dependency::Dependency(std::size_t Predecessor,
                       std::size_t Successor,
                       Dependency::Type DependencyType,
                       int LagDays)
    : m_uPredecessor(Predecessor),
      m_uSuccessor(Successor),
      m_Type(DependencyType),
      m_iLag(LagDays)
{
    if (IsSelfDependency()) {    //任务不能依赖自身，直接拒绝构造
        throw std::invalid_argument("Self-dependency is not allowed.");
    }
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::Dependency
//【函数功能】       拷贝构造依赖关系，复制前置、后置下标、依赖类型与延迟天数。
//【参数】           Source（输入参数）：被拷贝的依赖关系对象。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
Dependency::Dependency(const Dependency& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::operator=
//【函数功能】       拷贝赋值依赖关系，复制前置、后置下标、依赖类型与延迟天数。
