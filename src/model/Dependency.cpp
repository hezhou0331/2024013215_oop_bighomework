//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
Dependency::~Dependency() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::GetPredecessor
//【函数功能】       读取前置任务下标。
//【参数】           无
//【返回值】         返回前置任务在项目任务表中的下标。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
std::size_t Dependency::GetPredecessor() const
{
    return m_uPredecessor;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::GetSuccessor
//【函数功能】       读取后置任务下标。
//【参数】           无
//【返回值】         返回后置任务在项目任务表中的下标。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
std::size_t Dependency::GetSuccessor() const
{
    return m_uSuccessor;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::GetType
//【函数功能】       读取依赖类型。
//【参数】           无
//【返回值】         返回依赖类型枚举值（FS/SS/FF/SF 之一）。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
Dependency::Type Dependency::GetType() const
{
    return m_Type;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::GetLag
//【函数功能】       读取延迟天数 Lag。
//【参数】           无
//【返回值】         返回延迟天数，正值表示滞后，负值表示提前。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
int Dependency::GetLag() const
{
    return m_iLag;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::IsSelfDependency
//【函数功能】       判断本依赖是否为自依赖，即前置任务与后置任务为同一任务。
//【参数】           无
//【返回值】         自依赖返回 true，否则返回 false。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
bool Dependency::IsSelfDependency() const
{
    return m_uPredecessor == m_uSuccessor;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::HasTask
//【函数功能】       判断给定任务是否作为前置或后置参与本依赖。
//【参数】           TaskIndex（输入参数）：待检查的任务下标。
//【返回值】         该任务是本依赖的前置或后置时返回 true，否则返回 false。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
bool Dependency::HasTask(std::size_t TaskIndex) const
{
    return (m_uPredecessor == TaskIndex) || (m_uSuccessor == TaskIndex);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::HasEndpoints
//【函数功能】       判断本依赖是否恰好连接给定的前置、后置任务对（方向敏感）。
//【参数】           Predecessor（输入参数）：期望的前置任务下标；
//                   Successor（输入参数）：期望的后置任务下标。
//【返回值】         前置与后置均相符时返回 true，否则返回 false。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
bool Dependency::HasEndpoints(std::size_t Predecessor,
                              std::size_t Successor) const
{
    return (m_uPredecessor == Predecessor) && (m_uSuccessor == Successor);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::IsValidForTaskCount
//【函数功能】       判断本依赖在给定任务总数下是否合法：前置、后置下标均须小于任务
//                   总数，且不是自依赖。
//【参数】           TaskCount（输入参数）：项目中任务的总数。
//【返回值】         依赖合法返回 true，否则返回 false。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
bool Dependency::IsValidForTaskCount(std::size_t TaskCount) const
{
    return (m_uPredecessor < TaskCount)
        && (m_uSuccessor < TaskCount)
        && !IsSelfDependency();
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::GetSuccessorEarliestStart
//【函数功能】       CPM 正推核心换算：按依赖类型计算本依赖约束下后置任务允许的最早
//                   开始时间。FS：前置最早完成 + Lag；SS：前置最早开始 + Lag；
//                   FF：前置最早完成 + Lag - 后置工期（先约束最早完成再折算回开始）；
//                   SF：前置最早开始 + Lag - 后置工期（同理由完成折算回开始）。
//【参数】           Predecessor（输入参数）：前置任务，取其最早开始/最早完成时间；
//                   Successor（输入参数）：后置任务，FF/SF 折算时取其工期。
//【返回值】         返回后置任务在本依赖约束下允许的最早开始时间（第几天）。
//【开发者及日期】   刘江宇, 2026-07-05
//【修改记录】       2026-07-05 补充函数注释块并规范形参命名。
//-------------------------------------------------------------------------------------------------------------------
int Dependency::GetSuccessorEarliestStart(const Task& Predecessor,
                                          const Task& Successor) const
{
    if (m_Type == Dependency::Type::FINISH_TO_START) {
        //FS：后置最早开始 = 前置最早完成 + Lag
        return Predecessor.GetEF() + m_iLag;
    }
    if (m_Type == Dependency::Type::START_TO_START) {
        //SS：后置最早开始 = 前置最早开始 + Lag
        return Predecessor.GetES() + m_iLag;
    }
    if (m_Type == Dependency::Type::FINISH_TO_FINISH) {
        //FF：后置最早完成 = 前置最早完成 + Lag，折算回开始需再减后置工期
        return Predecessor.GetEF() + m_iLag - Successor.GetDuration();
    }
    //SF：后置最早完成 = 前置最早开始 + Lag，折算回开始需再减后置工期
    return Predecessor.GetES() + m_iLag - Successor.GetDuration();
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::GetPredecessorLatestStart
//【函数功能】       CPM 逆推核心换算：按依赖类型计算本依赖约束下前置任务允许的最晚
//                   开始时间。FS：后置最晚开始 - Lag - 前置工期（先约束前置最晚完成
//                   再折算回开始）；SS：后置最晚开始 - Lag；FF：后置最晚完成 - Lag -
//                   前置工期（同理折算）；SF：后置最晚完成 - Lag。
//【参数】           Predecessor（输入参数）：前置任务，FS/FF 折算时取其工期；
//                   Successor（输入参数）：后置任务，取其最晚开始/最晚完成时间。
//【返回值】         返回前置任务在本依赖约束下允许的最晚开始时间（第几天）。
//【开发者及日期】   刘江宇, 2026-07-05
//【修改记录】       2026-07-05 补充函数注释块并规范形参命名。
//-------------------------------------------------------------------------------------------------------------------
int Dependency::GetPredecessorLatestStart(const Task& Predecessor,
                                          const Task& Successor) const
{
    if (m_Type == Dependency::Type::FINISH_TO_START) {
        //FS：前置最晚完成 = 后置最晚开始 - Lag，折算回开始需再减前置工期
        return Successor.GetLS() - m_iLag - Predecessor.GetDuration();
    }
    if (m_Type == Dependency::Type::START_TO_START) {
        //SS：前置最晚开始 = 后置最晚开始 - Lag
        return Successor.GetLS() - m_iLag;
    }
    if (m_Type == Dependency::Type::FINISH_TO_FINISH) {
        //FF：前置最晚完成 = 后置最晚完成 - Lag，折算回开始需再减前置工期
        return Successor.GetLF() - m_iLag - Predecessor.GetDuration();
    }
    //SF：前置最晚开始 = 后置最晚完成 - Lag
    return Successor.GetLF() - m_iLag;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::GetTypeText
//【函数功能】       读取本依赖类型的文本表示。
//【参数】           无
//【返回值】         返回 "FS"/"SS"/"FF"/"SF" 之一。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
std::string Dependency::GetTypeText() const
{
    return TypeToText(m_Type);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::TypeFromText
//【函数功能】       把文本表示解析为依赖类型枚举，用于读取 PPM 文件中的依赖行。
//【参数】           Text（输入参数）：类型文本，合法值为 "FS"/"SS"/"FF"/"SF"。
//【返回值】         返回对应的 Dependency::Type 枚举值；未知文本抛出
//                   std::invalid_argument。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
Dependency::Type Dependency::TypeFromText(const std::string& Text)
{
    if (Text == "FS") {
        return Dependency::Type::FINISH_TO_START;
    }
    if (Text == "SS") {
        return Dependency::Type::START_TO_START;
    }
    if (Text == "FF") {
        return Dependency::Type::FINISH_TO_FINISH;
    }
    if (Text == "SF") {
        return Dependency::Type::START_TO_FINISH;
    }
    //四种合法文本以外的输入一律视为格式错误
    throw std::invalid_argument("Unknown dependency type: " + Text);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Dependency::TypeToText
//【函数功能】       把依赖类型枚举转换为文本表示，用于导出 PPM 文件与界面显示。
//【参数】           DependencyType（输入参数）：依赖类型枚举值。
//【返回值】         返回 "FS"/"SS"/"FF"/"SF" 之一；枚举已覆盖全部取值，末尾分支
//                   返回 "SF"。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
std::string Dependency::TypeToText(Dependency::Type DependencyType)
{
    if (DependencyType == Dependency::Type::FINISH_TO_START) {
        return "FS";
    }
    if (DependencyType == Dependency::Type::START_TO_START) {
        return "SS";
    }
    if (DependencyType == Dependency::Type::FINISH_TO_FINISH) {
        return "FF";
    }
    //剩余唯一可能取值为 START_TO_FINISH
    return "SF";
}
