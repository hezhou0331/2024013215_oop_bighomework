//【文件名】                 ValidationResult.cpp
//【功能模块和目的】         实现项目校验结果类：错误信息的累积记录、合法标志的维护与结果读取。
//【开发者及日期】           刘江宇, 2026-07-05
//【修改记录】               2026-07-05 按课程 C++ 编码规范 V1.3 修订注释与标识符命名。
//-------------------------------------------------------------------------------------------------------------------
#include "model/ValidationResult.hpp"

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ValidationResult::ValidationResult
//【函数功能】       默认构造校验结果：尚无任何错误，合法标志初始化为 true。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ValidationResult::ValidationResult()
    : m_bIsValid(true)
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ValidationResult::ValidationResult
//【函数功能】       拷贝构造校验结果，复制合法标志与错误信息列表。
//【参数】           Source（输入参数）：被拷贝的校验结果对象。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ValidationResult::ValidationResult(const ValidationResult& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ValidationResult::operator=
//【函数功能】       拷贝赋值校验结果，复制合法标志与错误信息列表。
//【参数】           Source（输入参数）：赋值来源的校验结果对象。
//【返回值】         返回自身引用，支持连续赋值。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ValidationResult& ValidationResult::operator=(const ValidationResult& Source)
    = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ValidationResult::~ValidationResult
//【函数功能】       析构校验结果对象；容器成员自动释放，无需额外清理。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
ValidationResult::~ValidationResult() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ValidationResult::IsValid
//【函数功能】       读取项目是否通过全部校验，即尚未记录任何错误信息。
//【参数】           无
//【返回值】         项目合法返回 true，存在错误返回 false。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
bool ValidationResult::IsValid() const
{
    return m_bIsValid;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ValidationResult::GetMessages
//【函数功能】       读取校验过程中累积的全部错误信息。
//【参数】           无
//【返回值】         返回错误信息文本列表的常引用，合法项目对应空列表。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
const std::vector<std::string>& ValidationResult::GetMessages() const
{
    return m_Messages;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ValidationResult::AddError
//【函数功能】       记录一条校验错误信息，并把整体结果标记为不合法。
//【参数】           Message（输入参数）：描述错误原因的文本。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
void ValidationResult::AddError(const std::string& Message)
{
    m_bIsValid = false;               //一旦出现错误，整体结论即为不合法
    m_Messages.push_back(Message);
}
