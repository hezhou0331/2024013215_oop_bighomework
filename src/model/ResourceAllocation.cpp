//【文件名】                 ResourceAllocation.cpp
//【功能模块和目的】         实现资源分配记录类，维护任务占用的资源索引与占用数量。
//【开发者及日期】           刘江宇, 2026-07-05
//【更改记录】               2026-07-05 按课程 C++ 编码规范 V1.3 修订注释格式并补齐函数注释。
//-------------------------------------------------------------------------------------------------------------------
#include "model/ResourceAllocation.hpp"
#include <stdexcept>

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::ResourceAllocation
//【函数功能】       默认构造资源分配记录：指向 0 号资源，占用数量为 1。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
ResourceAllocation::ResourceAllocation()
    : m_uResourceIndex(0),
      m_iQuantity(1)
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::ResourceAllocation
//【函数功能】       以资源索引与占用数量构造资源分配记录；数量非正时抛出异常。
//【参数】           ResourceIndex（输入参数）：被占用资源在项目资源容器中的索引；
//                   Quantity（输入参数）：占用该资源的数量，必须为正整数。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       2026-07-05 补充本函数注释块。
//-------------------------------------------------------------------------------------------------------------------
ResourceAllocation::ResourceAllocation(std::size_t ResourceIndex,
                                       int Quantity)
    : m_uResourceIndex(ResourceIndex),
      m_iQuantity(Quantity)
{
    if (m_iQuantity <= 0) {                           //占用数量必须为正整数
        throw std::invalid_argument("Resource quantity must be positive.");
    }
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::ResourceAllocation
//【函数功能】       拷贝构造资源分配记录：复制资源索引与占用数量。
//【参数】           Source（输入参数）：被拷贝的资源分配记录。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
ResourceAllocation::ResourceAllocation(const ResourceAllocation& Source)
    = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::operator=
//【函数功能】       拷贝赋值资源分配记录：复制资源索引与占用数量。
//【参数】           Source（输入参数）：赋值来源的资源分配记录。
//【返回值】         返回自身引用，支持连续赋值。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       2026-07-05 补充本函数注释块。
//-------------------------------------------------------------------------------------------------------------------
ResourceAllocation& ResourceAllocation::operator=(
    const ResourceAllocation& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::~ResourceAllocation
//【函数功能】       析构资源分配记录；无额外资源需要释放。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
ResourceAllocation::~ResourceAllocation() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::GetResourceIndex
//【函数功能】       返回被占用资源在项目资源容器中的索引。
//【参数】           无
//【返回值】         std::size_t，资源索引。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
std::size_t ResourceAllocation::GetResourceIndex() const
{
    return m_uResourceIndex;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::GetQuantity
//【函数功能】       返回占用该资源的数量。
//【参数】           无
//【返回值】         int，占用数量，正整数。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
int ResourceAllocation::GetQuantity() const
{
    return m_iQuantity;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ResourceAllocation::SetQuantity
//【函数功能】       设置占用数量；数量非正时抛出 std::invalid_argument 且不修改原值。
//【参数】           Quantity（输入参数）：新的占用数量，必须为正整数。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
void ResourceAllocation::SetQuantity(int Quantity)
{
    if (Quantity <= 0) {                              //占用数量必须为正整数
        throw std::invalid_argument("Resource quantity must be positive.");
    }
    m_iQuantity = Quantity;
}
