//-------------------------------------------------------------------------------------------------------------------
//【文件名】                 ScheduleResult.cpp
//【功能模块和目的】         实现 CPM 调度结果类：结果各字段的读取，以及供调度器填充总工期、
//                           拓扑序、关键路径与各任务时间参数的写入接口。
//【开发者及日期】           刘江宇, 2026-07-05
//【更改记录】               2026-07-05 按课程 C++ 编码规范 V1.3 修订注释格式。
//-------------------------------------------------------------------------------------------------------------------
#include "model/ScheduleResult.hpp"

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::ScheduleResult
//【函数功能】       默认构造调度结果：成功标志置为 false，总工期置零，各容器为空。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
ScheduleResult::ScheduleResult()
    : m_bIsSuccessful(false),
      m_iProjectDuration(0)
{
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::ScheduleResult
//【函数功能】       拷贝构造调度结果，复制成功标志、总工期、拓扑序、关键路径与时间表。
//【参数】           Source（输入参数）：被拷贝的调度结果对象。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
ScheduleResult::ScheduleResult(const ScheduleResult& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::operator=
//【函数功能】       拷贝赋值调度结果，复制成功标志、总工期、拓扑序、关键路径与时间表。
//【参数】           Source（输入参数）：赋值来源的调度结果对象。
//【返回值】         返回自身引用，支持连续赋值。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
ScheduleResult& ScheduleResult::operator=(const ScheduleResult& Source)
    = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::~ScheduleResult
//【函数功能】       析构调度结果对象；容器成员自动释放，无需额外清理。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
ScheduleResult::~ScheduleResult() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::IsSuccessful
//【函数功能】       读取本次调度是否成功完成的标志。
//【参数】           无
//【返回值】         调度成功返回 true，否则返回 false。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
bool ScheduleResult::IsSuccessful() const
{
    return m_bIsSuccessful;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::GetProjectDuration
//【函数功能】       读取项目总工期。
//【参数】           无
//【返回值】         返回项目总工期，单位：天。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
int ScheduleResult::GetProjectDuration() const
{
    return m_iProjectDuration;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::GetTopologicalOrder
//【函数功能】       读取任务的拓扑排序序列。
//【参数】           无
//【返回值】         返回拓扑序中任务下标序列的常引用。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
const std::vector<std::size_t>& ScheduleResult::GetTopologicalOrder() const
{
    return m_TopologicalOrder;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::GetCriticalPath
//【函数功能】       读取关键路径上的任务序列（按加入顺序即拓扑序排列）。
//【参数】           无
//【返回值】         返回关键任务下标序列的常引用。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
const std::vector<std::size_t>& ScheduleResult::GetCriticalPath() const
{
    return m_CriticalPath;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::GetTaskSchedules
//【函数功能】       读取全部任务的时间参数表。
//【参数】           无
//【返回值】         返回任务下标到 TaskScheduleInfo 映射的常引用。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
const std::map<std::size_t, TaskScheduleInfo>&
ScheduleResult::GetTaskSchedules() const
{
    return m_TaskSchedules;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::MarkSuccessful
//【函数功能】       把本次调度标记为成功，供调度器在计算完成后调用。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
void ScheduleResult::MarkSuccessful()
{
    m_bIsSuccessful = true;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::SetProjectDuration
//【函数功能】       设置项目总工期。
//【参数】           Duration（输入参数）：项目总工期，单位：天。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
void ScheduleResult::SetProjectDuration(int Duration)
{
    m_iProjectDuration = Duration;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::SetTopologicalOrder
//【函数功能】       设置任务的拓扑排序序列。
//【参数】           Order（输入参数）：拓扑序中任务下标的序列。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
void ScheduleResult::SetTopologicalOrder(const std::vector<std::size_t>& Order)
{
    m_TopologicalOrder = Order;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::AddCriticalTask
//【函数功能】       向关键路径追加一个关键任务（松弛时间为零的任务）。
//【参数】           TaskIndex（输入参数）：关键任务在项目任务表中的下标。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
void ScheduleResult::AddCriticalTask(std::size_t TaskIndex)
{
    m_CriticalPath.push_back(TaskIndex);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       ScheduleResult::SetTaskSchedule
//【函数功能】       记录指定任务的时间参数；同一任务重复设置时覆盖旧值。
//【参数】           TaskIndex（输入参数）：任务在项目任务表中的下标；
//                   ScheduleInfo（输入参数）：该任务的时间参数集合。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
void ScheduleResult::SetTaskSchedule(std::size_t TaskIndex,
                                     const TaskScheduleInfo& ScheduleInfo)
{
    m_TaskSchedules[TaskIndex] = ScheduleInfo;
}
