//【文件名】                 BasicTask.cpp
//【功能模块和目的】         实现普通任务类，提供正整数工期、可占用资源的具体任务行为。
//【开发者及日期】           刘江宇, 2026-07-05
//【修改记录】               2026-07-05 按课程 C++ 编码规范 V1.3 修订注释格式并规范形参命名。
//-------------------------------------------------------------------------------------------------------------------
#include "model/BasicTask.hpp"
#include <stdexcept>

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       BasicTask::BasicTask
//【函数功能】       以名称与工期构造普通任务；工期非正时抛出 std::invalid_argument。
//【参数】           Name（输入参数）：任务名称，不允许为空字符串；
//                   Duration（输入参数）：任务工期，必须为正整数，单位：天。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
BasicTask::BasicTask(const std::string& Name, int Duration)
    : Task(Name),
      m_iDuration(Duration)
{
    if (m_iDuration <= 0) {                           //普通任务工期必须为正整数
        throw std::invalid_argument("BasicTask duration must be positive.");
    }
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       BasicTask::BasicTask
//【函数功能】       拷贝构造普通任务：复制名称、工期、依赖关系与资源分配。
//【参数】           Source（输入参数）：被拷贝的普通任务对象。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
BasicTask::BasicTask(const BasicTask& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       BasicTask::operator=
//【函数功能】       拷贝赋值普通任务：复制名称、工期、依赖关系与资源分配。
//【参数】           Source（输入参数）：赋值来源的普通任务对象。
//【返回值】         返回自身引用，支持连续赋值。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
BasicTask& BasicTask::operator=(const BasicTask& Source) = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       BasicTask::~BasicTask
//【函数功能】       析构普通任务；无额外资源需要释放。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
BasicTask::~BasicTask() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       BasicTask::GetDuration
//【函数功能】       返回普通任务的工期。
//【参数】           无
//【返回值】         int，任务工期，正整数，单位：天。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
int BasicTask::GetDuration() const
{
    return m_iDuration;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       BasicTask::IsResourceAllocatable
//【函数功能】       判断本任务能否占用资源；普通任务总是可以占用资源。
//【参数】           无
//【返回值】         bool，恒为 true，表示普通任务可以占用资源。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
bool BasicTask::IsResourceAllocatable() const
{
    return true;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       BasicTask::Clone
//【函数功能】       多态复制：以拷贝构造生成自身的深拷贝，供项目容器复制任务时使用。
//【参数】           无
//【返回值】         std::unique_ptr<Task>，指向新建普通任务副本的独占指针。
//【开发者及日期】   刘江宇, 2026-07-05
//-------------------------------------------------------------------------------------------------------------------
std::unique_ptr<Task> BasicTask::Clone() const
{
    return std::make_unique<BasicTask>(*this);
}
