//【文件名】                 ConsoleUI.hpp
//【功能模块和目的】         声明控制台界面类，作为 MVC 中的视图层：显示菜单、读取输入、
//                           把控制器回传的状态枚举与信息类格式化为文本输出。
//【开发者及日期】           2024013215, 2026-07-05
//【更改记录】               2026-07-07 配合控制器"状态枚举 + 信息类"接口重构，格式化职责移入本类。
//-------------------------------------------------------------------------------------------------------------------
#ifndef PROJECT_SCHEDULER_CONSOLE_UI_HPP
#define PROJECT_SCHEDULER_CONSOLE_UI_HPP

//ProjectController 控制器类所属头文件（界面仅依赖控制器，不直接依赖模型层）
#include "controller/ProjectController.hpp"

//std::string 所属头文件
#include <string>

//-------------------------------------------------------------------------------------------------------------------
//【类名】             ConsoleUI
//【功能】             项目调度器的控制台界面（MVC 中的 V）：显示菜单、读取用户输入，把
//                     命令转发给控制器单例，并把控制器回传的状态枚举与信息类格式化为
//                     控制台文本；自身不包含任何业务逻辑。
//【接口说明】         对外仅提供 Execute() 启动交互主循环；菜单显示、输入读取、命令分发
//                     与结果格式化均为私有实现细节。
//【开发者及日期】     2024013215, 2026-07-05
//【更改记录】         2026-07-07 配合控制器接口重构，新增各类信息的格式化输出函数。
//-------------------------------------------------------------------------------------------------------------------
class ConsoleUI {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：界面无内部状态，无需特殊初始化
    ConsoleUI();
    // 拷贝构造函数：界面无内部状态，采用默认逐成员拷贝
    ConsoleUI(const ConsoleUI& Source);
    // 拷贝赋值运算符：界面无内部状态，采用默认逐成员赋值
    ConsoleUI& operator=(const ConsoleUI& Source);
    // 析构函数：界面不持有资源，无需额外清理
    ~ConsoleUI();

    //-----------------------------------------------------------------------------------------------------------
    //交互主循环
    //-----------------------------------------------------------------------------------------------------------
    // 启动控制台交互主循环，直到用户选择退出或输入结束
    void Execute();

private:
    //-----------------------------------------------------------------------------------------------------------
    //命令分发与各菜单项处理
    //-----------------------------------------------------------------------------------------------------------
    // 按菜单编号分发命令；返回 false 表示用户选择退出
    bool HandleChoice(int Choice) const;
    // 读取项目名称并新建空项目
    void CreateProject() const;
    // 读取文件名并导入项目模型（按扩展名自动选择导入器）
    void ImportProject() const;
    // 读取文件名并导出当前项目（按扩展名自动选择导出器）
    void ExportProject() const;
    // 读取名称与工期并添加任务（工期 0 自动创建里程碑）
    void AddTask() const;
    // 读取索引、新名称与新工期并修改任务
    void UpdateTask() const;
    // 读取索引并删除任务（连带其依赖与资源分配）
    void RemoveTask() const;
    // 格式化输出全部任务列表
    void ListTasks() const;
    // 读取索引并格式化输出该任务的前驱与后继列表
    void ListTaskRelations() const;
    // 读取前后置索引、类型与 Lag 并添加依赖
    void AddDependency() const;
    // 读取索引并删除依赖
    void RemoveDependency() const;
    // 读取前后置任务索引并删除依赖关系
    void RemoveDependencyByTaskPair() const;
    // 格式化输出全部依赖列表
    void ListDependencies() const;
    // 读取名称与单位成本并添加资源
    void AddResource() const;
    // 格式化输出全部资源列表
    void ListResources() const;
    // 读取任务索引、资源索引与数量并建立资源分配
    void AssignResource() const;
    // 校验当前项目合理性并格式化输出结论
    void ValidateProject() const;
    // 执行 CPM 调度计算并格式化输出总工期与关键路径
    void RunSchedule() const;
    // 汇总并格式化输出项目统计信息
    void ShowStatistics() const;

    //-----------------------------------------------------------------------------------------------------------
    //输出与输入工具
    //-----------------------------------------------------------------------------------------------------------
    // 输出全部菜单项
    void PrintMenu() const;
    // 输出失败状态的描述文本；HasDetail 为 true 时追加控制器 LastError 详情
    void PrintFailure(ProjectController::RES Result, bool HasDetail) const;
    // 输出一次调度结论（总工期与关键路径）
    void PrintSchedule(const ProjectController::ScheduleInfo& Info) const;
    // 显示提示并读取一个整数，非法输入时重新提示
    int ReadInt(const std::string& Prompt) const;
    // 显示提示并读取一个浮点数，非法输入时重新提示
    double ReadDouble(const std::string& Prompt) const;
    // 显示提示并读取一整行文本
    std::string ReadText(const std::string& Prompt) const;
    // 读取非负整数索引，负数输入时提示错误并重试
    std::size_t GetPositiveIndex(const std::string& Prompt) const;
};

#endif
