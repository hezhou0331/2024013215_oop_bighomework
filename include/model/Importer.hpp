//-------------------------------------------------------------------------------------------------------------------
//【类名】             Importer
//【功能】             导入器基类模板（模板参数 T 为目标模型类型）：定义"文件到模型对象"
//                     的统一导入流程与按扩展名注册/查找具体导入器的工厂机制，可脱离本
//                     作业用于任何需要多格式文件导入的应用。
//【接口说明】         LoadFromFile 校验扩展名与文件可读性后调用派生类 LoadFromStream 完成
//                     解析并返回 T 对象；LoadFromStream 为纯虚函数，由具体格式派生类实现；
//                     静态 Register<DERIVED> 注册一种具体导入器（扩展名不可重复）；静态
//                     GetInstanceByFileName/GetInstanceByExtName 按文件名/扩展名查找已注册
//                     导入器实例指针，未注册抛出 INVALID_FILE_TYPE；构造函数受保护，禁止
//                     拷贝与赋值；GetSupportedExtName 只读返回所支持扩展名。
//【开发者及日期】     刘江宇, 2026-07-07
//【更改记录】         2026-07-07 由非模板抽象基类重构为类模板，增加扩展名注册工厂。
//-------------------------------------------------------------------------------------------------------------------
template<class T>
class Importer : public FilePorter<FilePorterType::IMPORTER> {
public:
    //-----------------------------------------------------------------------------------------------------------
    //公有的析构、被删除的拷贝构造与赋值行为
    //-----------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Importer::Importer
    //【函数功能】       拷贝构造函数；导入器以注册实例形式共享使用，禁止复制实例，避免工厂登记表中出现语义不清的副本。
    //【参数】           Source（输入参数）：被拷贝的导入器对象。
    //【返回值】         无。
    //【开发者及日期】   刘江宇, 2026-07-07
    //【更改记录】       无
    //-------------------------------------------------------------------------------------------------------------------
    Importer(const Importer& Source) = delete;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Importer::operator=
    //【函数功能】       拷贝赋值运算符；导入器注册后由工厂统一管理，禁止对象间赋值。
    //【参数】           Source（输入参数）：赋值来源的导入器对象。
    //【返回值】         Importer<T>&，当前导入器对象引用；本函数被删除，不能被调用。
    //【开发者及日期】   刘江宇, 2026-07-07
    //【更改记录】       无
    //-------------------------------------------------------------------------------------------------------------------
    Importer& operator=(const Importer& Source) = delete;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Importer::~Importer
    //【函数功能】       虚析构函数；作为继承体系基类，保证通过基类指针释放时正确调用派生类析构函数。
    //【参数】           无
    //【返回值】         无。
    //【开发者及日期】   刘江宇, 2026-07-07
    //【更改记录】       无
    //-------------------------------------------------------------------------------------------------------------------
    virtual ~Importer();

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 校验扩展名与文件可读性后，把指定文件导入为 T 类型对象
    T LoadFromFile(const std::string& FileName) const;
    // 从已打开的文件流解析出 T 类型对象，由具体格式派生类实现
    virtual T LoadFromStream(std::ifstream& Stream) const = 0;
    // 返回本导入器支持的文件扩展名（不含 '.'）
    const std::string& GetSupportedExtName() const;

    //-----------------------------------------------------------------------------------------------------------
    //静态工厂成员函数
    //-----------------------------------------------------------------------------------------------------------
    // 注册一种具体导入器（DERIVED 须为 Importer<T> 的派生类，扩展名不可与已注册者重复）
    template<class DERIVED>
    static void Register();
    // 按文件名中的扩展名查找已注册导入器实例指针，未注册抛出 INVALID_FILE_TYPE
    static std::shared_ptr<Importer<T>> GetInstanceByFileName(
        const std::string& FileName);
    // 按扩展名（不区分大小写）查找已注册导入器实例指针，未注册抛出 INVALID_FILE_TYPE
    static std::shared_ptr<Importer<T>> GetInstanceByExtName(
        const std::string& FileExtName);

protected:
    //-----------------------------------------------------------------------------------------------------------
    //受保护的构造行为（仅允许派生类实例化）
    //-----------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       Importer::Importer
    //【函数功能】       带参构造函数；指定本导入器支持的文件扩展名，扩展名不可为空。
    //【参数】           FileExtName（输入参数）：本导入器支持的扩展名，不含点号。
    //【返回值】         无。
    //【开发者及日期】   刘江宇, 2026-07-07
    //【更改记录】       无
    //-------------------------------------------------------------------------------------------------------------------
    explicit Importer(const std::string& FileExtName);

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    std::string m_ExtName;    //本导入器支持的文件扩展名（不含 '.'）

    //所有已注册导入器实例指针的列表（静态工厂的登记表）
    static std::vector<std::shared_ptr<Importer<T>>> m_pImporters;
};

//-------------------------------------------------------------------------------------------------------------------
//以下为 Importer 类模板的实现部分（类模板的声明与实现均须位于头文件中，声明在前、实现在后）
//-------------------------------------------------------------------------------------------------------------------

//所有已注册导入器实例指针的列表（类模板静态数据成员定义）
template<class T>
std::vector<std::shared_ptr<Importer<T>>> Importer<T>::m_pImporters{};

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Importer::Importer
//【函数功能】       以支持的文件扩展名构造导入器基类部分；扩展名为空抛出异常。
//【参数】           FileExtName（输入参数）：本导入器支持的文件扩展名（不含 '.'）。
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-07
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
template<class T>
Importer<T>::Importer(const std::string& FileExtName)
    : FilePorter<FilePorterType::IMPORTER>(), m_ExtName(FileExtName)
{
    //扩展名是查找导入器的唯一依据，不允许为空
    if (FileExtName == "") {
        throw INVALID_FILE_TYPE("EMPTY");
    }
}

