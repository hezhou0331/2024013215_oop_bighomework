//【函数名称】       Importer::~Importer
//【函数功能】       析构导入器基类部分；不持有动态资源，无需额外清理。
//【参数】           无
//【返回值】         无。
//【开发者及日期】   刘江宇, 2026-07-07
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
template<class T>
Importer<T>::~Importer() = default;

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Importer::GetSupportedExtName
//【函数功能】       读取本导入器支持的文件扩展名。
//【参数】           无
//【返回值】         const std::string&，本导入器支持的文件扩展名（不含 '.'）。
//【开发者及日期】   刘江宇, 2026-07-07
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
template<class T>
const std::string& Importer<T>::GetSupportedExtName() const
{
    return m_ExtName;
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Importer::LoadFromFile
//【函数功能】       完成统一导入流程：校验文件扩展名、测试文件可读性、打开文件流，再
//                   调用派生类实现的 LoadFromStream 解析出模型对象。
//【参数】           FileName（输入参数）：待导入的文件路径。
//【返回值】         T，解析得到的模型对象；扩展名不符或文件不可读时抛出异常。
//【开发者及日期】   刘江宇, 2026-07-07
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
template<class T>
T Importer<T>::LoadFromFile(const std::string& FileName) const
{
    FileTypeTest(FileName, m_ExtName);    //扩展名不符抛出 INVALID_FILE_TYPE
    FileOpenTest(FileName);               //文件不可读抛出 FILE_OPEN_FAIL
    std::ifstream Stream(FileName);
    return LoadFromStream(Stream);        //具体解析交给派生类实现
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Importer::Register（静态）
//【函数功能】       注册一种具体导入器：实例化 DERIVED 对象并登记到导入器列表；DERIVED
//                   必须是 Importer<T> 的派生类，且扩展名不可与已注册导入器重复。
//【参数】           无（模板参数 DERIVED 指定被注册的具体导入器类型）。
//【返回值】         无；扩展名重复时抛出 INVALID_FILE_TYPE。
//【开发者及日期】   刘江宇, 2026-07-07
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
template<class T>
template<class DERIVED>
void Importer<T>::Register()
{
    //编译期断言：只允许注册 Importer<T> 的派生类
    static_assert(std::is_base_of<Importer<T>, DERIVED>::value,
                  "DERIVED must inherit from Importer<T>");
    std::shared_ptr<Importer<T>> pDerivedImporter
        = std::make_shared<DERIVED>();
    //扩展名查重（不区分大小写），同一扩展名只允许一个导入器
    for (const std::shared_ptr<Importer<T>>& pImporter : m_pImporters) {
        if (ToUpperCopy(pImporter->GetSupportedExtName())
            == ToUpperCopy(pDerivedImporter->GetSupportedExtName())) {
            throw INVALID_FILE_TYPE(pDerivedImporter->GetSupportedExtName());
        }
    }
    m_pImporters.push_back(pDerivedImporter);
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Importer::GetInstanceByFileName（静态）
//【函数功能】       截取文件名中的扩展名，查找并返回对应的已注册导入器实例指针。
//【参数】           FileName（输入参数）：待导入的文件路径。
//【返回值】         std::shared_ptr<Importer<T>>，对应导入器实例指针；未注册抛出
//                   INVALID_FILE_TYPE。
//【开发者及日期】   刘江宇, 2026-07-07
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
template<class T>
std::shared_ptr<Importer<T>> Importer<T>::GetInstanceByFileName(
    const std::string& FileName)
{
    return GetInstanceByExtName(GetExtName(FileName));
}

//-------------------------------------------------------------------------------------------------------------------
//【函数名称】       Importer::GetInstanceByExtName（静态）
//【函数功能】       按扩展名（不区分大小写）在已注册导入器列表中查找并返回实例指针。
//【参数】           FileExtName（输入参数）：文件扩展名（不含 '.'）。
//【返回值】         std::shared_ptr<Importer<T>>，对应导入器实例指针；未注册抛出
//                   INVALID_FILE_TYPE。
//【开发者及日期】   刘江宇, 2026-07-07
//【更改记录】       无
//-------------------------------------------------------------------------------------------------------------------
template<class T>
std::shared_ptr<Importer<T>> Importer<T>::GetInstanceByExtName(
    const std::string& FileExtName)
{
    //遍历登记表，扩展名比较不区分大小写
    for (const std::shared_ptr<Importer<T>>& pImporter : m_pImporters) {
        if (ToUpperCopy(pImporter->GetSupportedExtName())
            == ToUpperCopy(FileExtName)) {
            return pImporter;
        }
    }
    throw INVALID_FILE_TYPE(FileExtName);
}

