#ifndef TASK_SCHEDULE_INFO_HPP
#define TASK_SCHEDULE_INFO_HPP

//-------------------------------------------------------------------------------------------------------------------
//【类名】             TaskScheduleInfo
//【功能】             任务时间参数：封装单个任务的最早开始（ES）、最早完成（EF）、
//                     最晚开始（LS）、最晚完成（LF）与松弛时间（Slack），均以天计。
//【接口说明】         默认构造得到全零参数，或以五个时间值构造；GetES/GetEF/GetLS/
//                     GetLF/GetSlack 分别读取对应时间参数。对象一经构造即不可变。
//【开发者及日期】     刘江宇, 2026-07-05
//【修改记录】         2026-07-05 按课程 C++ 编码规范 V1.3 修订注释与成员命名。
//-------------------------------------------------------------------------------------------------------------------
class TaskScheduleInfo {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：五个时间参数全部初始化为零
    TaskScheduleInfo();
    // 带参构造函数：以最早开始/完成、最晚开始/完成与松弛时间构造
    TaskScheduleInfo(int EarlyStart, int EarlyFinish, int LateStart,
                     int LateFinish, int Slack);
    // 拷贝构造函数：复制全部时间参数
    TaskScheduleInfo(const TaskScheduleInfo& Source);
    // 拷贝赋值运算符：复制全部时间参数
    TaskScheduleInfo& operator=(const TaskScheduleInfo& Source);
    // 析构函数：无资源需要释放
    ~TaskScheduleInfo();

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 返回最早开始时间（天）
    int GetES() const;
    // 返回最早完成时间（天）
    int GetEF() const;
    // 返回最晚开始时间（天）
    int GetLS() const;
    // 返回最晚完成时间（天）
    int GetLF() const;
    // 返回松弛时间（天），为零表示关键任务
    int GetSlack() const;

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    int m_iEarlyStart;    //最早开始时间（天）
    int m_iEarlyFinish;   //最早完成时间（天）
    int m_iLateStart;     //最晚开始时间（天）
    int m_iLateFinish;    //最晚完成时间（天）
    int m_iSlack;         //松弛时间（天），为零即关键任务
};

#endif
