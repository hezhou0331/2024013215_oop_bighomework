#ifndef RESOURCE_ALLOCATION_HPP
#define RESOURCE_ALLOCATION_HPP
#include <cstddef>

//-------------------------------------------------------------------------------------------------------------------
//【类名】             ResourceAllocation
//【功能】             资源分配记录：描述一个任务对某项资源的占用，保存资源在项目资源
//                     容器中的索引以及占用数量（正整数），是任务资源列表的元素类型。
//【接口说明】         构造时可给定资源索引与数量（默认指向 0 号资源、数量 1）；
//                     GetResourceIndex 返回资源索引，GetQuantity/SetQuantity 读写
//                     占用数量（必须为正整数，非法值抛出异常）。
//【开发者及日期】     刘江宇, 2026-07-05
//【修改记录】         2026-07-05 按课程 C++ 编码规范 V1.3 修订注释格式。
//-------------------------------------------------------------------------------------------------------------------
class ResourceAllocation {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：指向 0 号资源，数量为 1
    ResourceAllocation();
    // 带参构造函数：以资源索引与占用数量构造分配记录，数量非正抛出异常
    ResourceAllocation(std::size_t ResourceIndex, int Quantity);
    // 拷贝构造函数：复制资源索引与占用数量
    ResourceAllocation(const ResourceAllocation& Source);
    // 拷贝赋值运算符：复制资源索引与占用数量
    ResourceAllocation& operator=(const ResourceAllocation& Source);
    // 析构函数：无额外资源需要释放
    ~ResourceAllocation();

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 返回被占用资源在项目资源容器中的索引
    std::size_t GetResourceIndex() const;
    // 返回占用该资源的数量
    int GetQuantity() const;

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Setter 成员函数
    //-----------------------------------------------------------------------------------------------------------
    // 设置占用数量，非正整数抛出异常
    void SetQuantity(int Quantity);

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    std::size_t m_uResourceIndex;    //被占用资源在项目资源容器中的索引
    int m_iQuantity;                 //占用该资源的数量，正整数
};

#endif
