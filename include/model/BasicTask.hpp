#ifndef BASIC_TASK_HPP
#define BASIC_TASK_HPP
#include "model/Task.hpp"

//-------------------------------------------------------------------------------------------------------------------
//【类名】             BasicTask
//【功能】             普通任务：具有正整数工期（单位：天），可以占用资源，是 Task 的
//                     具体派生类之一。
//【接口说明】         构造时给定名称与工期；GetDuration 返回工期，IsResourceAllocatable
//                     恒为 true，Clone 多态复制自身；名称、依赖关系与资源分配等
//                     通用接口继承自 Task。任务必须有名称与工期，
//                     故不提供默认构造函数。
//【开发者及日期】     刘江宇, 2026-07-05
//【更改记录】         2026-07-05 显式删除默认构造函数，并按编码规范 V1.3 重写注释。
//-------------------------------------------------------------------------------------------------------------------
class BasicTask : public Task {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 任务必须给定名称与工期才有意义，故不应存在默认构造函数
    BasicTask() = delete;
    // 带参构造函数：以名称与工期（正整数，单位：天）构造普通任务
    BasicTask(const std::string& Name, int Duration);
    // 拷贝构造函数：复制名称、工期、依赖关系与资源分配
    BasicTask(const BasicTask& Source);
    // 拷贝赋值运算符：复制名称、工期、依赖关系与资源分配
    BasicTask& operator=(const BasicTask& Source);
    // 析构函数：无额外资源需要释放
    ~BasicTask() override;

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 返回任务工期（天）
    int GetDuration() const override;
    // 普通任务可以占用资源，恒返回 true
    bool IsResourceAllocatable() const override;
    // 多态复制：返回自身的深拷贝
    std::unique_ptr<Task> Clone() const override;

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    int m_iDuration;    //任务工期，正整数，单位：天
};

#endif
