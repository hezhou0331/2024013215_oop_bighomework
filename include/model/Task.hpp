//【文件名】                 Task.hpp
//【功能模块和目的】         声明任务抽象基类，管理名称、依赖关系、资源分配与调度时间。
//【开发者及日期】           刘江宇, 2026-07-05
//【更改记录】               2026-07-05 按课程 C++ 编码规范 V1.3 修订注释格式并重命名调度时间成员。
//-------------------------------------------------------------------------------------------------------------------
#ifndef TASK_HPP
#define TASK_HPP
#include "model/ResourceAllocation.hpp"
#include <memory>
#include <string>
#include <vector>

// 前置声明：Resource 类在 Resource.hpp 中定义
class Resource;

//-------------------------------------------------------------------------------------------------------------------
//【类名】             Task
//【功能】             任务抽象基类：统一管理任务名称、前驱后继依赖索引、资源分配列表
//                     以及关键路径法（CPM）调度时间（最早/最晚开始与结束、时差），
//                     由 BasicTask 与 MilestoneTask 派生出具体任务类型。
//【接口说明】         GetName/SetName 读写任务名称（不允许为空）；GetDuration、
//                     IsResourceAllocatable、Clone 为纯虚函数，由派生类给出
//                     工期、能否占资源与多态复制的具体行为；AddResource/
//                     RemoveResource/ClearResources/GetResources 维护资源分配；
//                     SetPredecessors/SetSuccessors 及对应 Get 维护依赖索引；
//                     SetSchedule/ClearSchedule 与 GetES/GetEF/GetLS/GetLF/GetSlack/
//                     IsCritical 读写调度计算结果；CalculateTotalCost 按资源单价
//                     汇总任务总成本。构造函数受保护，仅供派生类使用。
//【开发者及日期】     刘江宇, 2026-07-05
//【更改记录】         2026-07-05 按课程 C++ 编码规范 V1.3 修订注释格式并重命名调度时间成员。
//-------------------------------------------------------------------------------------------------------------------
class Task {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 虚析构函数：保证通过基类指针删除派生类对象时行为正确
    virtual ~Task();

    // 拷贝构造函数：复制名称、依赖关系、资源分配与调度时间
    Task(const Task& Source);
    // 拷贝赋值运算符：复制名称、依赖关系、资源分配与调度时间
    Task& operator=(const Task& Source);

    //-----------------------------------------------------------------------------------------------------------
    //任务名称的读写
    //-----------------------------------------------------------------------------------------------------------
    // 返回任务名称
    const std::string& GetName() const;
    // 设置任务名称，名称为空时抛出异常
    void SetName(const std::string& Name);

    //-----------------------------------------------------------------------------------------------------------
    //由派生类实现的多态行为（纯虚函数）
    //-----------------------------------------------------------------------------------------------------------
    // 纯虚函数：返回任务工期（天），由派生类实现
    virtual int GetDuration() const = 0;
    // 纯虚函数：判断该任务能否占用资源，由派生类实现
    virtual bool IsResourceAllocatable() const = 0;
    // 纯虚函数：多态复制，返回自身的深拷贝
    virtual std::unique_ptr<Task> Clone() const = 0;

    //-----------------------------------------------------------------------------------------------------------
    //资源分配与成本计算
    //-----------------------------------------------------------------------------------------------------------
    // 按资源单价、占用数量与工期汇总本任务的总成本
    double CalculateTotalCost(const std::vector<Resource>& Resources) const;

    // 为任务分配指定索引的资源；已分配则更新数量
    void AddResource(std::size_t ResourceIndex, int Quantity);
    // 移除对指定索引资源的分配记录
    void RemoveResource(std::size_t ResourceIndex);
    // 清空本任务的全部资源分配
    void ClearResources();
    // 返回本任务的资源分配列表
    const std::vector<ResourceAllocation>& GetResources() const;

    //-----------------------------------------------------------------------------------------------------------
    //前驱与后继依赖索引的维护
    //-----------------------------------------------------------------------------------------------------------
    // 设置前驱任务索引列表
    void SetPredecessors(const std::vector<std::size_t>& Predecessors);
    // 设置后继任务索引列表
    void SetSuccessors(const std::vector<std::size_t>& Successors);
    // 返回前驱任务索引列表
    const std::vector<std::size_t>& GetPredecessors() const;
    // 返回后继任务索引列表
    const std::vector<std::size_t>& GetSuccessors() const;

    //-----------------------------------------------------------------------------------------------------------
    //CPM 调度时间的读写
    //-----------------------------------------------------------------------------------------------------------
    // 写入 CPM 调度结果（最早/最晚开始与结束时间），并同步计算时差
    void SetSchedule(int EarlyStart, int EarlyFinish,
                     int LateStart, int LateFinish);
    // 把调度时间重置为初始状态（各时间归零、结束时间取工期）
    void ClearSchedule();
    // 返回最早开始时间 ES（天）
    int GetES() const;
    // 返回最早结束时间 EF（天）
    int GetEF() const;
    // 返回最晚开始时间 LS（天）
    int GetLS() const;
    // 返回最晚结束时间 LF（天）
    int GetLF() const;
    // 返回时差 Slack = LS - ES（天）
    int GetSlack() const;
    // 判断任务是否位于关键路径上（时差为 0）
    bool IsCritical() const;

protected:
    //-----------------------------------------------------------------------------------------------------------
    //受保护的构造行为与工具函数
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：仅供派生类使用，名称取占位值，调度时间归零
    Task();
    // 带参构造函数：以名称构造任务，名称为空时抛出异常
    explicit Task(const std::string& Name);

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    std::string m_Name;                          //任务名称，非空字符串
    std::vector<ResourceAllocation> m_Resources; //资源分配列表（资源索引与数量）
    std::vector<std::size_t> m_Predecessors;     //前驱任务在项目容器中的索引列表
    std::vector<std::size_t> m_Successors;       //后继任务在项目容器中的索引列表
    int m_iEarlyStart;                           //最早开始时间 ES，单位：天
    int m_iEarlyFinish;                          //最早结束时间 EF，单位：天
    int m_iLateStart;                            //最晚开始时间 LS，单位：天
    int m_iLateFinish;                           //最晚结束时间 LF，单位：天
    int m_iSlack;                                //时差 Slack = LS - ES，单位：天
};

#endif
