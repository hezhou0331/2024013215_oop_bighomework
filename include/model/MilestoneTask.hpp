#ifndef MILESTONE_TASK_HPP
#define MILESTONE_TASK_HPP
#include "model/Task.hpp"

//-------------------------------------------------------------------------------------------------------------------
//【类名】             MilestoneTask
//【功能】             里程碑任务：工期恒为 0 天，不可占用资源，用于标记项目某个阶段
//                     的开始或结束，是 Task 的具体派生类之一。
//【接口说明】         构造时仅需给定名称；GetDuration 恒返回 0，IsResourceAllocatable
//                     恒为 false，Clone 多态复制自身；名称与依赖关系等通用接口
//                     继承自 Task。任务必须有名称，故不提供默认构造函数。
//【开发者及日期】     刘江宇, 2026-07-05
//【更改记录】         2026-07-05 显式删除默认构造函数，并按编码规范 V1.3 重写注释。
//-------------------------------------------------------------------------------------------------------------------
class MilestoneTask : public Task {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       MilestoneTask::MilestoneTask
    //【函数功能】       默认构造函数；里程碑任务必须具有名称，禁止构造信息不完整的任务对象。
    //【参数】           无
    //【返回值】         无。
    //【开发者及日期】   刘江宇, 2026-07-05
    //【更改记录】       无
    //-------------------------------------------------------------------------------------------------------------------
    MilestoneTask() = delete;
    // 带参构造函数：以名称构造里程碑任务，工期恒为 0
    explicit MilestoneTask(const std::string& Name);
    // 拷贝构造函数：复制名称与依赖关系
    MilestoneTask(const MilestoneTask& Source);
    // 拷贝赋值运算符：复制名称与依赖关系
    MilestoneTask& operator=(const MilestoneTask& Source);
    // 析构函数：无额外资源需要释放
    ~MilestoneTask() override;

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 里程碑工期恒为 0 天
    int GetDuration() const override;
    // 里程碑不可占用资源，恒返回 false
    bool IsResourceAllocatable() const override;
    // 多态复制：返回自身的深拷贝
    std::unique_ptr<Task> Clone() const override;
};

#endif
