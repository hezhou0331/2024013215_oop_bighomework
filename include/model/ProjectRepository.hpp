#ifndef PROJECT_REPOSITORY_HPP
#define PROJECT_REPOSITORY_HPP

//Project 项目模型类所属头文件
#include "model/Project.hpp"

//-------------------------------------------------------------------------------------------------------------------
//【类名】             ProjectRepository
//【功能】             项目仓库类：持有应用当前唯一的"当前项目"对象，为上层控制器提供
//                     统一的读取、整体替换与重置入口，隔离项目对象的存放细节。
//【接口说明】         GetCurrentProject 返回当前项目引用（区分只读与可修改版本）；
//                     SetCurrentProject 以拷贝或移动方式整体替换当前项目（导入文件后
//                     使用）；Reset 把当前项目重置为默认名称的空项目。
//【开发者及日期】     2024013215, 2026-07-05
//【更改记录】         2026-07-05 按课程 C++ 编码规范 V1.3 修订注释与标识符命名。
//-------------------------------------------------------------------------------------------------------------------
class ProjectRepository {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：以默认名称 "UntitledProject" 创建空的当前项目
    ProjectRepository();
    // 拷贝构造函数：深拷贝仓库持有的当前项目
    ProjectRepository(const ProjectRepository& Source);
    // 拷贝赋值运算符：深拷贝仓库持有的当前项目
    ProjectRepository& operator=(const ProjectRepository& Source);
    // 析构函数：当前项目为值成员，无需额外清理
    ~ProjectRepository();

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（可修改与只读两个版本，只读版本有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 返回当前项目的可修改引用
    Project& GetCurrentProject();
    // 返回当前项目的只读引用
    const Project& GetCurrentProject() const;

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Setter 成员函数
    //-----------------------------------------------------------------------------------------------------------
    // 以拷贝方式整体替换当前项目
    void SetCurrentProject(const Project& SourceProject);
    // 以移动方式整体替换当前项目
    void SetCurrentProject(Project&& SourceProject);
    // 把当前项目重置为默认名称的空项目
    void Reset();

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    Project m_CurrentProject;    //当前正在编辑的项目对象
};

#endif
