//【功能模块和目的】         声明任务依赖关系类（含内嵌依赖类型枚举），表达前置任务与后置
//                           任务之间 FS/SS/FF/SF 四种时序约束及延迟天数（Lag）。
//【开发者及日期】           刘江宇, 2026-07-05
