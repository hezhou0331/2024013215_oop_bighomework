#ifndef DEPENDENCY_HPP
#define DEPENDENCY_HPP
#include <cstddef>
#include <string>

// 前置声明：Task 类在 Task.hpp 中定义
class Task;

//-------------------------------------------------------------------------------------------------------------------
//【类名】             Dependency
//【功能】             任务依赖关系：记录前置任务下标、后置任务下标、依赖类型与延迟
//                     天数（Lag，可为负表示提前），并提供 CPM 计算所需的时间换算。
//【接口说明】         内嵌枚举 Type 表示 FS/SS/FF/SF 四种依赖类型；构造时给定前置、
//                     后置、类型与 Lag（禁止自依赖）；Get 系列读取各字段；
//                     IsSelfDependency / HasTask / HasEndpoints / IsValidForTaskCount
//                     用于合法性与匹配判断；GetSuccessorEarliestStart /
//                     GetPredecessorLatestStart 按依赖类型换算 CPM 正推、逆推的时间
//                     约束；TypeFromText / TypeToText 完成类型与 "FS"/"SS"/"FF"/"SF"
//                     文本的互相转换。
//【开发者及日期】     刘江宇, 2026-07-05
//【修改记录】         2026-07-05 按课程 C++ 编码规范 V1.3 修订注释与标识符命名。
//                     2026-07-07 依赖类型枚举内嵌入类。
//-------------------------------------------------------------------------------------------------------------------
class Dependency {
public:
    //-----------------------------------------------------------------------------------------------------------
    //内嵌枚举类型
    //-----------------------------------------------------------------------------------------------------------
    //【枚举名】Type【功能】两任务间的四种依赖类型：FS 前置完成后后置才能开始；
    //SS 前置开始后后置才能开始；FF 前置完成后后置才能完成；SF 前置开始后后置才能完成
    //【开发者及日期】刘江宇, 2026-07-05
    enum class Type {
        FINISH_TO_START,    //FS：前置完成 -> 后置开始
        START_TO_START,     //SS：前置开始 -> 后置开始
        FINISH_TO_FINISH,   //FF：前置完成 -> 后置完成
        START_TO_FINISH     //SF：前置开始 -> 后置完成
    };

    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：构造 0 号任务到 0 号任务的 FS、零延迟占位依赖
    Dependency();
    // 带参构造函数：给定前置、后置任务下标、依赖类型与延迟天数；自依赖抛出异常
    Dependency(std::size_t Predecessor,
               std::size_t Successor,
               Type DependencyType,
               int LagDays);
    // 拷贝构造函数：复制前置、后置下标、依赖类型与延迟天数
    Dependency(const Dependency& Source);
    // 拷贝赋值运算符：复制前置、后置下标、依赖类型与延迟天数
    Dependency& operator=(const Dependency& Source);
    // 析构函数：无额外资源需要释放
    ~Dependency();

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 返回前置任务下标
    std::size_t GetPredecessor() const;
    // 返回后置任务下标
    std::size_t GetSuccessor() const;
    // 返回依赖类型枚举值
    Type GetType() const;
    // 返回延迟天数 Lag（可为负）
    int GetLag() const;

    // 判断是否为自依赖（前置与后置为同一任务）
    bool IsSelfDependency() const;
    // 判断给定任务是否作为前置或后置参与本依赖
    bool HasTask(std::size_t TaskIndex) const;
    // 判断本依赖是否恰好连接给定的前置、后置任务对
    bool HasEndpoints(std::size_t Predecessor, std::size_t Successor) const;
    // 判断前置、后置下标是否均在任务总数范围内且非自依赖
    bool IsValidForTaskCount(std::size_t TaskCount) const;

    // CPM 正推：按依赖类型换算后置任务允许的最早开始时间
    int GetSuccessorEarliestStart(const Task& Predecessor,
                                  const Task& Successor) const;
    // CPM 逆推：按依赖类型换算前置任务允许的最晚开始时间
    int GetPredecessorLatestStart(const Task& Predecessor,
                                  const Task& Successor) const;

    // 返回本依赖类型的文本表示（"FS"/"SS"/"FF"/"SF"）
    std::string GetTypeText() const;

    //-----------------------------------------------------------------------------------------------------------
    //静态成员函数
    //-----------------------------------------------------------------------------------------------------------
    // 把文本 "FS"/"SS"/"FF"/"SF" 解析为依赖类型枚举，未知文本抛出异常
    static Type TypeFromText(const std::string& Text);
    // 把依赖类型枚举转换为文本 "FS"/"SS"/"FF"/"SF"
    static std::string TypeToText(Type DependencyType);

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    std::size_t m_uPredecessor;    //前置任务在项目任务表中的下标
    std::size_t m_uSuccessor;      //后置任务在项目任务表中的下标
    Type m_Type;                   //依赖类型（FS/SS/FF/SF）
    int m_iLag;                    //延迟天数，可为负表示提前
};

#endif
