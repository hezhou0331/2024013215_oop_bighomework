#ifndef RESOURCE_HPP
#define RESOURCE_HPP

//std::string 所属头文件
#include <string>

//-------------------------------------------------------------------------------------------------------------------
//【类名】             Resource
//【功能】             项目资源：由名称与单位时间成本（非负）描述，可被普通任务按数量
//                     占用，用于计算任务与项目的资源成本。
//【接口说明】         构造时可给定名称与单位成本（默认取占位名与 0 成本）；GetName/
//                     SetName 读写名称（不允许为空），GetUnitCost/SetUnitCost 读写
//                     单位成本（不允许为负）；CalculateCost 按数量与工期计算占用
//                     成本；HasSameName 判断名称是否与给定字符串相同，用于查重。
//【开发者及日期】     2024013215, 2026-07-05
//【更改记录】         2026-07-05 按课程 C++ 编码规范 V1.3 修订注释格式并规范形参命名。
//-------------------------------------------------------------------------------------------------------------------
class Resource {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：名称取占位值，单位成本为 0
    Resource();
    // 带参构造函数：以名称与单位成本构造资源，非法值抛出异常
    Resource(const std::string& Name, double UnitCost);
    // 拷贝构造函数：复制名称与单位成本
    Resource(const Resource& Source);
    // 拷贝赋值运算符：复制名称与单位成本
    Resource& operator=(const Resource& Source);
    // 析构函数：无额外资源需要释放
    ~Resource();

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 返回资源名称
    const std::string& GetName() const;
    // 返回资源单位时间成本
    double GetUnitCost() const;

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Setter 成员函数
    //-----------------------------------------------------------------------------------------------------------
    // 设置资源名称，名称为空时抛出异常
    void SetName(const std::string& Name);
    // 设置资源单位时间成本，负值抛出异常
    void SetUnitCost(double UnitCost);

    //-----------------------------------------------------------------------------------------------------------
    //成本计算与名称查重（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 按占用数量与工期计算成本：单位成本 * 数量 * 工期
    double CalculateCost(int Quantity, int Duration) const;
    // 判断资源名称是否与给定名称相同
    bool HasSameName(const std::string& Name) const;

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    std::string m_Name;     //资源名称，非空字符串
    double m_rUnitCost;     //资源单位时间成本，非负实数，单位：元/（个·天）
};

#endif
