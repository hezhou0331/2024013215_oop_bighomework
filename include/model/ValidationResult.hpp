#ifndef VALIDATION_RESULT_HPP
#define VALIDATION_RESULT_HPP
#include <string>
#include <vector>

//-------------------------------------------------------------------------------------------------------------------
//【类名】             ValidationResult
//【功能】             项目校验结果：初始视为合法，每记录一条错误信息即转为不合法，
//                     并保留全部错误文本供界面逐条显示。
//【接口说明】         IsValid 返回项目是否通过全部校验；GetMessages 返回累积的错误
//                     信息列表；AddError 记录一条错误并把结果标记为不合法。
//【开发者及日期】     刘江宇, 2026-07-05
//【更改记录】         2026-07-05 按课程 C++ 编码规范 V1.3 修订注释与标识符命名。
//-------------------------------------------------------------------------------------------------------------------
class ValidationResult {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：构造尚无错误、视为合法的初始结果
    ValidationResult();
    // 拷贝构造函数：复制合法标志与错误信息列表
    ValidationResult(const ValidationResult& Source);
    // 拷贝赋值运算符：复制合法标志与错误信息列表
    ValidationResult& operator=(const ValidationResult& Source);
    // 析构函数：容器成员自动释放，无需额外清理
    ~ValidationResult();

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（均有后置 const）
    //-----------------------------------------------------------------------------------------------------------
    // 返回项目是否通过全部校验（尚未记录任何错误）
    bool IsValid() const;
    // 返回累积的错误信息列表
    const std::vector<std::string>& GetMessages() const;

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Setter 成员函数
    //-----------------------------------------------------------------------------------------------------------
    // 记录一条错误信息并把结果标记为不合法
    void AddError(const std::string& Message);

private:
    //-----------------------------------------------------------------------------------------------------------
    //私有数据成员
    //-----------------------------------------------------------------------------------------------------------
    bool m_bIsValid;                       //是否合法：尚未记录错误时为 true
    std::vector<std::string> m_Messages;   //累积的错误信息文本列表
};

#endif
