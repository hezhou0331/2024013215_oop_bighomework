//【文件名】                 PPMExporter.hpp
//【功能模块和目的】         声明 PPM 格式导出器，把内存中的 Project 对象按 PPM 文本格式写入文件。
//【开发者及日期】           刘江宇, 2026-07-05
//【更改记录】               2026-07-07 基类重构为 Exporter<Project> 模板，改为实现流写出接口。
