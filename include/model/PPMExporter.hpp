#ifndef PPM_EXPORTER_HPP
#define PPM_EXPORTER_HPP
#include "model/Exporter.hpp"
#include "model/Project.hpp"
#include <fstream>

//-------------------------------------------------------------------------------------------------------------------
//【类名】             PPMExporter
//【功能】             PPM 格式导出器：Exporter<Project> 的具体派生类，把项目模型按 PPM
//                     文本格式依次写出项目名行、任务块、资源块、依赖块与分配块；扩展名
//                     固定为 "PPM"。
//【接口说明】         默认构造函数向基类登记扩展名 "PPM"；SaveToStream 把给定项目模型
//                     写入已打开的文件流；文件打开与扩展名校验由基类 SaveToFile 统一
//                     完成；导出器无内部状态，禁止拷贝构造与拷贝赋值。
//【开发者及日期】     刘江宇, 2026-07-05
//【更改记录】         2026-07-07 基类重构为 Exporter<Project> 模板，改为实现流写出接口。
//-------------------------------------------------------------------------------------------------------------------
class PPMExporter : public Exporter<Project> {
public:
    //-----------------------------------------------------------------------------------------------------------
    //必要的构造、析构、赋值行为
    //-----------------------------------------------------------------------------------------------------------
    // 默认构造函数：向基类登记本导出器支持的扩展名 "PPM"
    PPMExporter();
    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       PPMExporter::PPMExporter
    //【函数功能】       拷贝构造函数；PPM 导出器以注册实例形式共享使用，禁止复制实例。
    //【参数】           Source（输入参数）：被拷贝的 PPM 导出器对象。
    //【返回值】         无。
    //【开发者及日期】   刘江宇, 2026-07-07
    //【更改记录】       无
    //-------------------------------------------------------------------------------------------------------------------
    PPMExporter(const PPMExporter& Source) = delete;

    //-------------------------------------------------------------------------------------------------------------------
    //【函数名称】       PPMExporter::operator=
    //【函数功能】       拷贝赋值运算符；PPM 导出器注册后由工厂统一管理，禁止对象间赋值。
    //【参数】           Source（输入参数）：赋值来源的 PPM 导出器对象。
    //【返回值】         PPMExporter&，当前导出器对象引用；本函数被删除，不能被调用。
    //【开发者及日期】   刘江宇, 2026-07-07
    //【更改记录】       无
    //-------------------------------------------------------------------------------------------------------------------
    PPMExporter& operator=(const PPMExporter& Source) = delete;
    // 析构函数：导出器不持有资源，无需额外清理
    ~PPMExporter() override;

    //-----------------------------------------------------------------------------------------------------------
    //非静态 Getter 成员函数（有后置 const，不改变导出器自身状态）
    //-----------------------------------------------------------------------------------------------------------
    // 按 PPM 格式把给定项目模型写入已打开的文件流
    void SaveToStream(std::ofstream& Stream,
                      const Project& SourceProject) const override;
};

#endif
